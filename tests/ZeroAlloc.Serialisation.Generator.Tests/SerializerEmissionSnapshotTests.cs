// Locks down the exact emitted source text for six representative
// (format × shape) combinations. Any accidental drift in the generator
// output — a new `#nullable` directive, spacing change, different using
// order — fails these tests loudly. When intentional, update the
// corresponding Expected_* constant to match the new emission.
//
// The comparison normalises line endings (CRLF → LF) so the tests are
// stable across platforms / git autocrlf settings.
