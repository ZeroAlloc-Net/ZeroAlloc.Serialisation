    // ── helpers ──────────────────────────────────────────────────────────────

    private static string EmitSerializer(string source, string hintName)
    {
        var tree = CSharpSyntaxTree.ParseText(source);
        var zeroAllocRef = MetadataReference.CreateFromFile(
            typeof(ZeroAlloc.Serialisation.ZeroAllocSerializableAttribute).Assembly.Location);
        var refs = new System.Collections.Generic.List<MetadataReference>(
            Basic.Reference.Assemblies.Net100.References.All) { zeroAllocRef };
        var compilation = CSharpCompilation.Create(
            "TestAssembly",
            new[] { tree },
            refs,
            new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary));
        var driver = CSharpGeneratorDriver.Create(new SerializerGenerator())
            .RunGenerators(compilation);
        var result = driver.GetRunResult();
        var file = result.GeneratedTrees
            .FirstOrDefault(t => t.FilePath.Contains(hintName, StringComparison.Ordinal));
        Assert.NotNull(file);
        return file!.GetText().ToString();
    }

    private static void AssertSnapshot(string expected, string actual)
    {
        var e = Normalise(expected);
        var a = Normalise(actual);
        Assert.Equal(e, a);
    }

    // Normalise line endings so the snapshot survives CRLF/LF differences
    // across platforms and git autocrlf configurations.
    private static string Normalise(string s)
        => s.Replace("\r\n", "\n", StringComparison.Ordinal)
            .Replace("\r",   "\n", StringComparison.Ordinal)
            .Trim();
