    [Fact]
    public void MemoryPack_PlainClass_EmitsExactSource()
    {
        const string source = """
            using ZeroAlloc.Serialisation;
            namespace Demo;
            [ZeroAllocSerializable(SerializationFormat.MemoryPack)]
            public sealed class MpBasic { public string V { get; set; } = ""; }
            """;

        var actual = EmitSerializer(source, "MpBasicSerializer.g.cs");
        AssertSnapshot(Expected_MemoryPack_PlainClass, actual);
    }

